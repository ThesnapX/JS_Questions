// Declare a variable named counter and set its value to 10.
// Use the increment operator (++) to increase counter by 1.
// Then, use the decrement operator (--) to decrease counter by 1.
// Print the value of counter to the console after each operation.

// Pre Increment
let counter = 10;
let increment = counter++
console.log("the increment value is " + increment);

// Pre Decrement
let decrement = counter--
console.log("the decrement value is " + decrement);


// Post Increment
let postCounter = 10;
let postIncrement = ++counter
console.log("the increment value is " + postIncrement);

// Post Decrement
let postDecrement = --counter
console.log("the decrement value is " + postDecrement);

